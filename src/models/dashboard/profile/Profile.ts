import mongoose from "mongoose";

const { Schema, model } = mongoose;

const userProfileSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      trim: true,
    },
    confirmPassword: {
      type: String,
      required: true,
    },
    role: {
      type: String,
      enum: ["user", "admin"],
      default: "user",
    },
    status: {
      type: String,
      enum: ["active", "inactive"],
      default: "active",
    },

    bio: {
      type: String,
      trim: true,
      min: 10,
      max: 100,
    },
    avatar: {
      type: String,
      trim: true,
      default: "https://i.pravatar.cc/300",
    },
    createdAt: {
      type: Date,
      default: Date.now,
    },
    updatedAt: {
      type: Date,
      default: Date.now,
    },
  },
  {
    timestamps: true,
  }
);

const Profile = model("Profile", userProfileSchema);

export default Profile;
